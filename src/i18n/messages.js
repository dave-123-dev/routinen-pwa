export function normalizeLanguage(value) {
  return String(value || 'de').slice(0, 2) === 'en' ? 'en' : 'de';
}

export function messagesFor(lang) {
  return MESSAGES[normalizeLanguage(lang)] || MESSAGES.de;
}
